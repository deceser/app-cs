void ShowCheckersBoard() // Функція для відображення шашкової дошки
{
    int size = 8; // Встановлюємо розмір дошки 8x8
    bool showCoordinates = true; // Прапорець для відображення координат (використовуємо bool)
    string[,] board = new string[size, size]; // Створюємо двовимірний масив для дошки

    for (int i = 0; i < size; i++) // Проходимо по всіх рядках дошки
    {
        for (int j = 0; j < size; j++) // Проходимо по всіх стовпцях дошки
        {
            board[i, j] = "."; // Заповнюємо кожну клітинку крапкою (порожня клітинка)
        }
    }

    for (int i = 0; i < 3; i++) // Проходимо по перших трьох рядках (для чорних шашок)
    {
        for (int j = 0; j < size; j++) // Проходимо по всіх стовпцях
        {
            if ((i + j) % 2 != 0) // Перевіряємо, чи клітинка чорна (сума індексів непарна)
            {
                board[i, j] = "b"; // Ставимо чорну шашку
            }
            else // Якщо клітинка біла (використовуємо else)
            {
                board[i, j] = "."; // Залишаємо порожню клітинку
            }
        }
    }

    int row = 5; // Початковий рядок для білих шашок
    while (row < 8) // Використовуємо while замість for для останніх трьох рядків
    {
        for (int j = 0; j < size; j++) // Проходимо по всіх стовпцях
        {
            if ((row + j) % 2 != 0) // Перевіряємо, чи клітинка чорна
            {
                board[row, j] = "w"; // Ставимо білу шашку
            }
            else // Якщо клітинка біла
            {
                board[row, j] = "."; // Залишаємо порожню клітинку
            }
        }
        row++; // Збільшуємо лічильник рядка
    }

    if (showCoordinates) // Перевіряємо, чи потрібно виводити координати
    {
        Console.Write("  "); // Відступ для координат стовпців
        for (int k = 0; k < size; k++) // Виводимо номери стовпців
        {
            Console.Write(k + " "); // Виводимо номер стовпця
        }
        Console.WriteLine(); // Переходимо на новий рядок
    }

    for (int i = 0; i < size; i++) // Проходимо по всіх рядках для виводу
    {
        if (showCoordinates) // Якщо потрібні координати
        {
            Console.Write(i + " "); // Виводимо номер рядка
        }

        for (int j = 0; j < size; j++) // Проходимо по всіх стовпцях для виводу
        {
            string cellSymbol = ""; // Змінна для символу клітинки

            switch (board[i, j]) // Використовуємо switch для вибору символу
            {
                case "b": // Якщо чорна шашка
                    cellSymbol = "b"; // Встановлюємо символ чорної шашки
                    break; // Виходимо з switch
                case "w": // Якщо біла шашка
                    cellSymbol = "w"; // Встановлюємо символ білої шашки
                    break; // Виходимо з switch
                case ".": // Якщо порожня клітинка
                    cellSymbol = "."; // Встановлюємо символ порожньої клітинки
                    break; // Виходимо з switch
                default: // Якщо інший символ (на всяк випадок)
                    cellSymbol = "?"; // Встановлюємо символ питання
                    break; // Виходимо з switch
            }

            Console.Write(cellSymbol + " "); // Виводимо символ клітинки з пробілом
        }
        Console.WriteLine(); // Переходимо на новий рядок після виведення повного рядка дошки
    }

    if (showCoordinates) // Якщо включені координати
    {
        Console.WriteLine(); // Додатковий відступ після дошки
    }
}

ShowCheckersBoard(); // Викликаємо функцію для відображення дошки
